export type DiversityMode = 'off' | 'soft' | 'full';

/** Normalize env / caller string to a DiversityMode. */
export function parseDiversityMode(raw: string | undefined): DiversityMode {
  const m = (raw ?? 'soft').toLowerCase().trim();
  if (m === 'off' || m === '0' || m === 'false') return 'off';
  if (m === 'full' || m === 'strong') return 'full';
  return 'soft';
}

