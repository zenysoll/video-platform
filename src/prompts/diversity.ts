async function slotHash32(streamId: string, batchIndex: number, sequenceNum: number): Promise<number> {
  const hex = await sha256Hex(`${streamId}|${batchIndex}|${sequenceNum}`);
  return parseInt(hex.slice(0, 8), 16) >>> 0;
}

/**
 * Deterministic hints for one job slot. Returns null if diversity off.
 *
 * plannerLines now includes THREE mandatory dimensions:
 *   1. Scene archetype — WHERE the video takes place (228-entry pool)
 *   2. Subject domain  — WHAT the main subject is (80-entry pool, no hands/POV bias)
 *   3. Pace/energy     — HOW IT FEELS (200-entry pool; always in full, 50/50 in soft)
 */
export async function stableSlotHints(
  streamId: string,
  batchIndex: number,
  sequenceNum: number,
  mode: DiversityMode,
): Promise<SlotHints | null> {
  if (mode === 'off') return null;

  const h = await slotHash32(streamId, batchIndex, sequenceNum);
  const iArc  = h % SCENE_ARCHETYPE_HINTS.length;
  const iOpen = (Math.imul(h, 2654435761) >>> 0) % OPENING_FRAMING_HINTS.length;
  const iPace = (h >>> 16) % PACE_ENERGY_HINTS.length;
  const iSubj = (Math.imul(h, 1597334677) >>> 0) % SUBJECT_DOMAIN_HINTS.length;

  const archetype    = SCENE_ARCHETYPE_HINTS[iArc]!;
  const opening      = OPENING_FRAMING_HINTS[iOpen]!;
  const pace         = PACE_ENERGY_HINTS[iPace]!;
  const subjectDomain = SUBJECT_DOMAIN_HINTS[iSubj]!;

  if (mode === 'soft') {
    const addPace = (h & 2) === 0;
    const plannerLines = addPace
      ? `${archetype} Main subject: ${subjectDomain}. Overall energy: ${pace}.`
      : `${archetype} Main subject: ${subjectDomain}.`;
    return {
      plannerLines,
      rendererFrameEmphasis: opening,
    };
  }

  // full
  return {
    plannerLines: `${archetype} Main subject: ${subjectDomain}. Pacing feel: ${pace}.`,
    rendererFrameEmphasis: opening,
  };
}
